package com.soarclient.stk.api;

import java.awt.Color;

public abstract class ColorPalette {
	public abstract Color getPrimary();
	public abstract Color getPrimaryContainer();
	public abstract Color getSecondary();
	public abstract Color getSecondaryContainer();
	public abstract Color getTertiary();
	public abstract Color getTertiaryContainer();
	public abstract Color getSurface();
	public abstract Color getSurfaceContainer();
	public abstract Color getOnSurface();
}
